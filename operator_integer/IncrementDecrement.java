package operator_integer;

public class IncrementDecrement {
	public static void main(String[] args) {
		int x = 10;
		int y = 15;
		System.out.println("x = " + x);
		System.out.println("y = " + y);
		System.out.println();
		System.out.println("++x = " + ++x);
		System.out.println("y++ = " + y++);
		System.out.println();
		System.out.println("x = " + x);
		System.out.println("y = " + y);
		System.out.println();
		System.out.println("--x = " + --x);
		System.out.println("y-- = " + y--);
		System.out.println();
		System.out.println("x = " + x);
		System.out.println("y = " + y);
	}
}